void dump_arity_dist(void) {
  int i;
  for (i = 0; i < 45; i++)
    if (arity_dist[i] != 0)
      printf("%d: %d\n", i, arity_dist[i]);
}
#endif

gs_void_t
_gs_s (gs_t node, const gs_string_t s, gs_unsigned_long_t length)
{
  GS_ASSERT (node != (gs_t) NULL, "Got null node");
  node->u1.s = (gs_string_t) __gs_mempool_alloc (IB_STRING_ARENA, length);
  memcpy (gs_s(node), s, length);
  _gs_slen (node, length);
  return;
}

