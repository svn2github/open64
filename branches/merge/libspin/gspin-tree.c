// no carriage return is printed
static void
gs_dump_leaf(gs_t t, FILE *f,
	     gs_code_t parent_code, int parent_code_class, int index_in_parent)
{
  int j;
  if (t == (gs_t) NULL)
    return;
  fprintf (f, "%s<%d> ", gs_code_name (gs_code (t)), 
  	   gs_mempool_address2byteofst(GS_ARENA, (char *)t));
  switch (gs_code (t)) {
    case GS_ERROR_MARK:
      break;

    case IB_STRING:
      fprintf (f, "\"");
      for (j = 0; j < gs_slen(t); j++)
	if (*(j + gs_s (t)) != '\0')
	  fputc(*(j + gs_s (t)), f);
      fprintf (f, "\" ");
      break;

    case IB_UNSIGNED:
      fprintf (f, "%u ", gs_u (t));
      break;

    case IB_LONG_LONG:
      fprintf (f, "%lld ", gs_ll (t));
      break;

    case IB_UNSIGNED_LONG_LONG:
      fprintf (f, "%llu ", gs_ull (t));
      break;

    case IB_INT:
      fprintf (f, "%d ", gs_n (t));
      break;

    case IB_FLOAT:
      fprintf (f, "%f ", gs_f (t));
      break;

    case IB_DOUBLE:
      fprintf (f, "%f ", gs_d (t));
      break;

    case GS_TCC:
      fprintf (f, "%s ", gs_tree_code_class_name ((gs_tree_code_class_t) gs_b (t)));
      break;

    case GSBI:
      if (GSBI_BUILT_IN_ACOS <= (gsbi_t) gs_hword(t) && (gsbi_t) gs_hword(t) <= GSBI_END_BUILTINS) {
	fprintf (f, "%s ", gsbi_name ((gsbi_t) gs_hword(t)));
      }
      else if (GSBI_IX86_BUILTIN_ADDPS <= ((gsbi_ts_t) gs_hword(t)) && ((gsbi_ts_t) gs_hword(t)) <= GSBI_IX86_BUILTIN_MAX) {
	fprintf (f, "%s ", gsbi_ts_name ((gsbi_ts_t) gs_hword(t)));
      }
      else {
	fprintf (f, "GSBI_BUILT_IN_ACOS: %d, GSBI_END_BUILTINS: %d, GSBI_IX86_BUILTIN_ADDPS:%d, GSBI_IX86_BUILTIN_MAX: %d"
		" Warning: Unhandled GSBI case: %d ", 
		 GSBI_BUILT_IN_ACOS, GSBI_END_BUILTINS, GSBI_IX86_BUILTIN_ADDPS, GSBI_IX86_BUILTIN_MAX,
		 gs_hword(t));
      }
      break;

    case GSBI_CLASS:
      fprintf (f, "%s ", gsbi_class_name ((gsbi_class_t) gs_b (t)));
      break;

    case IB_LONG:
      fprintf (f, "%d ", gs_b(t));
      break;

    case IB_LONG_DOUBLE:
      fprintf (f, "%LF ", (long double) gs_ld (t));
      break;

    case IB_UNSIGNED_LONG:
      fprintf (f, "0x%x ", gs_u(t));
      break;

    case IB_BOOL:
      fprintf (f, "%s ", gs_b (t) == true ? "gs_true" : "gs_false");
      break;

    case IB_CHAR:
      fprintf(f, "%c ", gs_b(t));
      break;

    case IB_BIT_VECTOR:
      for (j = 0; j < sizeof (gs_long_long_t) * 8; j++) {
	if (gs_bv (t, j) == gs_true) {
	  fprintf(f, "%s ",
		  gs_flag_name(parent_code, index_in_parent, j,
			       parent_code_class));
	}
      }
      break;

    default:
      GS_ASSERT (gs_false, "unexpected 0-arity tree code");
      break;
  }
}

static void
gs_dump_core(gs_t t, gs_count_t *indentation, FILE *f, bool concise,
	     gs_code_t parent_code, int parent_code_class, int index_in_parent,
	     bool indent)
{
  static int function_nest_level = 0;
  gs_count_t i, j;

  if (t == (gs_t) NULL)
    return;

  if (indent)
    for (i = 0; i < *indentation; i++) fprintf (f, " ");

  if (gs_em (t) == true ||
      (gs_code(t) == GS_FUNCTION_DECL && function_nest_level > 0 &&
       gs_decl_saved_tree(t) != NULL)) {
    fprintf (f, "LINK %d\n", gs_mempool_address2byteofst(GS_ARENA, (char *)t));
    return;
  }

  _gs_em (t, true);

  if (gs_code_arity(gs_code(t)) == 0) {
    gs_dump_leaf(t, f, parent_code, parent_code_class, index_in_parent);
    fprintf(f, "\n");
    return;
  }

  fprintf (f, "%s<%d> ", gs_code_name (gs_code (t)), 
  	   gs_mempool_address2byteofst(GS_ARENA, (char *)t));

  switch (gs_code (t)) {
    case EMPTY:
      for (i = 0; i < *indentation; i++) fprintf (f, " ");
      fprintf (f, "[]\n");
      break;

    case CONS: {
	for (i = 0; i < *indentation; i++) fprintf (f, " ");
	fprintf (f, "[\n");
	(*indentation)++;

	gs_t p;
	for (p = t; gs_code(p) != EMPTY; p = gs_operand(p, 1))
	  gs_dump_core (gs_operand(p, 0), indentation, f, concise, parent_code,
			parent_code_class, index_in_parent, true);

	(*indentation)--;
	for (i = 0; i < *indentation; i++) fprintf (f, " ");
	fprintf (f, "]\n");
      }
      break;

    case GS_PROGRAM:
      fprintf (f, "{\n");
      (*indentation)++;
      for (j = 0; j < gs_code_arity(gs_code(t)); j++) {
	if (gs_operand(t, j) != NULL) {
	  gs_string_t p;
	  gs_int_t omit = 0;
	  p = gs_attribute_name(j, gs_b(gs_operand(t, 0)), gs_code(t), &omit);
	  for (i = 0; i < *indentation; i++)
	    fprintf (f, " ");
	  fprintf (f, "@");
	  for ( ; *p != '\0'; p++)
	    fprintf (f, "%c", tolower (*p));
	  fprintf (f, " ");
	  gs_dump_core(gs_operand(t, j), indentation, f, concise, 
		       gs_code(t), gs_b(gs_operand(t, GS_TREE_CODE_CLASS)),
		       j, false);
	}
      }
      (*indentation)--;
      for (i = 0; i < *indentation; i++) fprintf (f, " ");
      fprintf (f, "}\n");
      break;

    default:
      fprintf (f, "{ ");
      (*indentation)++;
      // do leaf kids first without inserting carriage returns
      for (j = 0; j < gs_code_arity(gs_code(t)); j++) {
	if (gs_operand(t, j) != NULL && 
	    gs_code_arity(gs_code(gs_operand(t, j))) == 0) {
	  fprintf(f, "(%d)", j);
	  gs_dump_leaf(gs_operand(t, j), f, gs_code(t),
		       gs_b(gs_operand(t, GS_TREE_CODE_CLASS)), j);
	}
      }
      fprintf (f, "\n");
      // now do non-leaf kids but skip GS_TREE_CHAIN
      if (gs_code(t) == GS_FUNCTION_DECL)
	function_nest_level++;
      for (j = GS_TREE_CHAIN-1; j < gs_code_arity(gs_code(t)); j++) {
	if (j == GS_TREE_CHAIN)
	  continue;
	if (gs_operand(t, j) != NULL && 
	    gs_code_arity(gs_code(gs_operand(t, j))) != 0) {
	  gs_string_t p;
	  gs_int_t omit = 0;
	  p = gs_attribute_name(j, gs_b(gs_operand(t, 0)), gs_code(t), &omit);
	  // gs_attribute_name returns NULL if the constructor does not have
	  // an attribute name for this attribute.  This can legitimately
	  // happen if the arg is a dummy arg put there to make the tree have
	  // the correct number of args for the tree code class (tcc).  If
	  // so, the arg should be NULL.
	  if (p != NULL && (!concise || !omit)) {
	    for (i = 0; i < *indentation; i++)
	      fprintf (f, " ");
	    fprintf (f, "@");
	    for ( ; *p != '\0'; p++)
	      fprintf (f, "%c", tolower (*p));
	    fprintf (f, " ");
	    gs_dump_core(gs_operand(t, j), indentation, f, concise, 
			 gs_code(t), gs_b(gs_operand(t, GS_TREE_CODE_CLASS)),
			 j, false);
	  }
	}
      }
      // now do GS_TREE_CHAIN
      if (gs_operand(t, GS_TREE_CHAIN) != NULL && 
	  gs_code_arity(gs_code(gs_operand(t, GS_TREE_CHAIN))) != 0) {
	gs_string_t p;
	gs_int_t omit = 0;
	p = gs_attribute_name(GS_TREE_CHAIN, gs_b(gs_operand(t, 0)), gs_code(t), &omit);
	// gs_attribute_name returns NULL if the constructor does not have
	// an attribute name for this attribute.  This can legitimately
	// happen if the arg is a dummy arg put there to make the tree have
	// the correct number of args for the tree code class (tcc).  If
	// so, the arg should be NULL.
	if (p != NULL && (!concise || !omit)) {
	  for (i = 0; i < *indentation; i++)
	    fprintf (f, " ");
	  fprintf (f, "@");
	  for ( ; *p != '\0'; p++)
	    fprintf (f, "%c", tolower (*p));
	  fprintf (f, " ");
	  gs_dump_core(gs_operand(t, GS_TREE_CHAIN), indentation, f, concise, 
		       gs_code(t), gs_b(gs_operand(t, GS_TREE_CODE_CLASS)),
		       GS_TREE_CHAIN, false);
	}
      }
      if (gs_code(t) == GS_FUNCTION_DECL)
	function_nest_level--;
      (*indentation)--;
      for (i = 0; i < *indentation; i++) fprintf (f, " ");
      fprintf (f, "}\n");
      break;
  }
}

gs_void_t
gs_dump (gs_t t) 
{ 
  gs_count_t indentation;
  indentation = 0;
  gs_dump_core (t, &indentation, stdout, true, 0, 0, 0, false);
}

gs_void_t
gs_dump_file (gs_t t, FILE *f) 
{ 
  gs_count_t indentation;
  indentation = 0;
  gs_dump_core (t, &indentation, f, false, 0, 0, 0, false);
}

