gs_t
__gs (gs_code_t code)
{
  GS_ASSERT (code != DOT, "__gs() called with DOT");
  int size = gspin_node_size(code);
  gs_t node = (gs_t) __gs_mempool_alloc(GS_ARENA, size);
  _gs_code(node, code);
  if (gs_mempool[GS_ARENA].current_index >= 0x3fffffff) {
    fprintf(stderr, "gspin mempool overflow.\n");
    abort();
  }
#ifdef Is_True_On
  num_gspin_nodes++;
  arity_dist[tree_code_table[code].arity]++;
#endif
  return node;
}

